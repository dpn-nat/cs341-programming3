class ValueIterationAgent(ValueEstimationAgent):
    """
        * Please read learningAgents.py before reading this.*

        A ValueIterationAgent takes a Markov decision process
        (see mdp.py) on initialization and runs value iteration
        for a given number of iterations using the supplied
        discount factor.
    """
    def __init__(self, mdp, discount = 0.9, iterations = 100):
        """
          Your value iteration agent should take an mdp on
          construction, run the indicated number of iterations
          and then act according to the resulting policy.

          Some useful mdp methods you will use:
              mdp.getStates()
              mdp.getPossibleActions(state)
              mdp.getTransitionStatesAndProbs(state, action)
              mdp.getReward(state, action, nextState)
              mdp.isTerminal(state)
        """
        self.mdp = mdp
        self.discount = discount
        self.iterations = iterations
        self.values = util.Counter() # A Counter is a dict with default 0
        self.runValueIteration()

    def runValueIteration(self):
        # Write value iteration code here
        # this runs the value iterations for the specified number of iterations
        for w in range(self.iterations):
            # create a new counter for the updated vals
            neVal = util.Counter()

            # lopps through all the states
            for stat in self.mdp.getStates():
                # if the state is terminal, then the value is 0 since there will not exist any future rewards
                if self.mdp.isTerminal(stat):
                    neVal[stat] = 0
                    continue
                
                # this gets the posisble actions that can be taken
                acts = self.mdp.getPossibleActions(stat)

                # this is to store the qvalues for each action if they exist
                qVal = []
                for act in acts:
                    q = self.computeQValueFromValues(stat, act)
                    qVal.append(q)

                # if they are valid then it would choose the maximum
                if qVal:
                    neVal[stat] = max(qVal)
            # after proccessin, it updates the value function
            self.values = neVal


    def getValue(self, state):
        """
          Return the value of the state (computed in __init__).
        """
        return self.values[state]


    def computeQValueFromValues(self, state, action):
        """
          Compute the Q-value of action in state from the
          value function stored in self.values.
        """
        # this initializes the qvalues for the state and action pair
        qVal = 0

        transit = self.mdp.getTransitionStatesAndProbs(state, action)
        # loops thorugh each possible next state
        for nexStat, prob in transit:
            # this gets the immediate reward for transitioning
            reward = self.mdp.getReward(state, action, nexStat)

            qVal += prob * (reward + self.discount * self.values[nexStat])

        # returns the computed qvalue for the state and action pair
        return qVal

    def computeActionFromValues(self, state):
        """
          The policy is the best action in the given state
          according to the values currently stored in self.values.

          You may break ties any way you see fit.  Note that if
          there are no legal actions, which is the case at the
          terminal state, you should return None.
        """
        # if the state is terminal, then there are no actions to take
        if self.mdp.isTerminal(state):
            return None

        # gets the possible actions that can be taken from the state
        acts = self.mdp.getPossibleActions(state)

        bestAct = None
        # starts with very small number
        besVal = float('-inf')

        # loops through possible actions
        for act in acts:
            q = self.computeQValueFromValues(state, act)
            # if action has higher qval then the current, then update
            if q > besVal:
                besVal = q
                bestAct = act
        # return the action with the highest qvalue.
        return bestAct

    def getPolicy(self, state):
        return self.computeActionFromValues(state)

    def getAction(self, state):
        "Returns the policy at the state (no exploration)."
        return self.computeActionFromValues(state)

    def getQValue(self, state, action):
        return self.computeQValueFromValues(state, action)

